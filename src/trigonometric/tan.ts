/**
 * @title tan
 * @description 正切
 * @param value {number}
 * @returns {number}
 */
export function tan(value: number) {
	return Math.tan(value)
}

/**
 * @title atan
 * @description 反正切
 * @param value {number}
 * @returns {number}
 */
export function atan(value: number) {
	return Math.atan(value)
}

/**
 * @title atan2
 * @description 从原点 (0,0) 到 (x,y) 点的线段与 x 轴正方向之间的平面角度 (弧度值)
 * @param y {number}
 * @param x {number}
 * @returns {number}
 */
export function atan2(y: number, x: number) {
	return Math.atan2(y, x)
}

/**
 * @title atan
 * @description 反双曲正切
 * @param value {number}
 * @returns {number}
 */
export function atanh(value: number) {
	return Math.atanh(value)
}