/**
 * @title cos
 * @description 余弦
 * @param value {number}
 * @returns {number}
 */
export function cos(value: number) {
	return Math.cos(value)
}

/**
 * @title acos
 * @description 反余弦
 * @param value {number}
 * @returns {number}
 */
export function acos(value: number) {
	return Math.acos(value)
}

/**
 * @title acos
 * @description 反双曲余弦
 * @param value {number}
 * @returns {number}
 */
export function acosh(value: number) {
	return Math.acosh(value)
}