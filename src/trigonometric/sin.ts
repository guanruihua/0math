/**
 * @title sin
 * @description 正弦
 * @param value {number}
 * @returns {number}
 */
export function sin(value: number) {
	return Math.sin(value)
}

/**
 * @title asin
 * @description 反正弦
 * @param value {number}
 * @returns {number}
 */
export function asin(value: number) {
	return Math.asin(value)
}

/**
 * @title asin
 * @description 反双曲正弦
 * @param value {number}
 * @returns {number}
 */
export function asinh(value: number) {
	return Math.asinh(value)
}